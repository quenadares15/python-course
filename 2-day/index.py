# INTEGER int()
first = input("First Number: ")
second = input("Second Number: ")
print(int(first) + int(second))
input("\nPress Enter to continue\n")
# int() convert string to integer

# FLOATING POINT NUMBER float()
float1 = input("First Number: ")
float2 = input("Second Number: ")
print(float(float1) + float(float2))
input("\nPress Enter to continue\n")
# In float() you can add two number with pointer
# For example, 2.2 + 2.2 give result 4.4 

# STRING str()
str1 = input("First Number / Word: ")
str2 = input("Second Number / Word: ")
print(str(str1) + str(str2))
input("\nType Enter to exit\n")
# str() is used to connecting strings together
# For example, in str() 2+2 give result 22 (not 4)
# In int(), 2+2 give result 4

